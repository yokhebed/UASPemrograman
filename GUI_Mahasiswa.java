    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // Ambil input dari text field
    String nim = txtNim.getText().trim();
    String nama = txtNama.getText().trim();
    String prodi = txtProdi.getText().trim();
    String angkatan = txtAngkatan.getText().trim();
    String alamat = txtAlamat.getText().trim();
    String jenisKelamin = radiobtnLaki.isSelected() ? radiobtnLaki.getText()
                          : radiobtnPerempuan.isSelected() ? radiobtnPerempuan.getText() : "";

    // Validasi input tidak boleh kosong
    if (nim.isEmpty() || nama.isEmpty() || prodi.isEmpty() || angkatan.isEmpty() || alamat.isEmpty() || jenisKelamin.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Validasi angka
    if (!nim.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "NIM hanya boleh angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (!angkatan.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "Angkatan hanya boleh angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Buat objek Mahasiswa
    Mahasiswa mhs = new Mahasiswa();
    mhs.dataNIM(nim);
    mhs.dataNama(nama);
    mhs.dataJenisKelamin(jenisKelamin);
    mhs.dataProdi(prodi);
    mhs.dataAngkatan(angkatan);
    mhs.dataAlamat(alamat);

    // Ambil model tabel
    DefaultTableModel dataModel = (DefaultTableModel) table_data_mahasiswa.getModel();
    List<String> list = new ArrayList<>();
    table_data_mahasiswa.setAutoCreateColumnsFromModel(true);

    // Masukkan data ke list
    list.add(mhs.cetakNIM());
    list.add(mhs.cetakNama());
    list.add(mhs.cetakJenisKelamin());
    list.add(mhs.cetakProdi());
    list.add(mhs.cetakAngkatan());
    list.add(mhs.cetakAlamat());

    // Tambah ke tabel
    dataModel.addRow(list.toArray());

    // Tampilkan pesan sukses
    JOptionPane.showMessageDialog(null, "Data anda berhasil ditambahkan ke tabel");

    // Bersihkan form input
    clear();
    }//GEN-LAST:event_btnSimpanActionPerformed

