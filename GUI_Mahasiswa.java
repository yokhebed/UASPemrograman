    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = table_data_mahasiswa.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Pilih baris yang ingin diedit terlebih dahulu.");
        return;
    }

    // Ambil data dari baris yang dipilih dan masukkan ke dalam field input
    txtNim.setText(table_data_mahasiswa.getValueAt(selectedRow, 0).toString());
    txtNama.setText(table_data_mahasiswa.getValueAt(selectedRow, 1).toString());

    String jenisKelamin = table_data_mahasiswa.getValueAt(selectedRow, 2).toString();
    if (jenisKelamin.equalsIgnoreCase("Laki-Laki")) {
        radiobtnLaki.setSelected(true);
    } else {
        radiobtnPerempuan.setSelected(true);
    }

    txtProdi.setText(table_data_mahasiswa.getValueAt(selectedRow, 3).toString());
    txtAngkatan.setText(table_data_mahasiswa.getValueAt(selectedRow, 4).toString());
    txtAlamat.setText(table_data_mahasiswa.getValueAt(selectedRow, 5).toString());

    // Hapus baris lama (akan diganti ketika klik Simpan lagi)
    DefaultTableModel model = (DefaultTableModel) table_data_mahasiswa.getModel();
    model.removeRow(selectedRow);

    }//GEN-LAST:event_btnEditActionPerformed

    private void txtNimKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNimKeyTyped
        char c = evt.getKeyChar();
    if (!Character.isDigit(c) && !Character.isISOControl(c)) {
        evt.consume(); // Menolak karakter yang bukan angka
        JOptionPane.showMessageDialog(null, "NIM hanya boleh diisi angka!");
    }
    }//GEN-LAST:event_txtNimKeyTyped

